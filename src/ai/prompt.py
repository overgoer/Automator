"""
Система промптов для генерации контента.
На основе анализа канала @eddytester.
"""

from typing import List


# Системный промпт - роль и контекст
SYSTEM_PROMPT = """
Ты — опытный контент-мейкер для Telegram канала о тестировании QA.
Канал: @eddytester

Стиль канала:
- Технический контент без воды
- Дружелюбный тон с элементами юмора
- Используй эмодзи уместно: 🤘 🔘 ✅ ❌ 🚀 💡 🧠 📌
- Хэштеги только по делу: #memes #оффтоп #ДорогойБаг
- Структура: заголовок → основной текст → ссылки/выводы
- Обращение на "ты" к читателю
- Короткие абзацы, легко читать с телефона

Твоя задача:
- Анализировать исходные посты из канала
- Создавать новый пост в том же стиле
- Сохранять техническую точность
- Добавлять эмодзи для акцентов
- Форматировать для Telegram (Markdown)
"""

# Промпт для генерации поста на основе последних сообщений
POST_GENERATION_PROMPT = """
Проанализируй следующие последние посты из канала:

{posts_text}

---

ЗАДАЧА:
Создай новый пост для канала в стиле @eddytester.

Требования:
1. Тема: основана на последних постах (продолжение дискуссии или новая смежная тема)
2. Длина: 500-1500 символов
3. Структура:
   - Цепляющий заголовок (можно с эмодзи)
   - Основной текст с абзацами
   - Выводы или призыв к действию
   - Хэштеги если уместно (1-2)
4. Эмодзи: 3-7 штук, уместно по тексту
5. Форматирование: используй **жирный** и _курсив_ для акцентов

Важно:
- Не копируй исходные посты, создавай новый контент
- Сохраняй стиль автора (технически, но с душой)
- Если в исходных постах есть ссылки — можешь упомянуть похожие темы

Верни ТОЛЬКО текст поста, без комментариев.
"""

# Промпт для саммари нескольких постов
SUMMARY_PROMPT = """
Проанализируй следующие посты из канала:

{posts_text}

---

ЗАДАЧА:
Сделай саммари основных идей этих постов.

Формат:
- Заголовок "📌 Главное за последние посты"
- 3-5 буллетпоинтов с основными мыслями
- Каждый буллет: 1-2 предложения
- В конце: вопрос или призыв к обсуждению

Длина: до 800 символов.
"""

# Промпт для дайджеста (недельная подборка)
DIGEST_PROMPT = """
Проанализируй посты за период:

{posts_text}

---

ЗАДАЧА:
Создай недельный дайджест для канала.

Структура:
1. Заголовок с эмодзи и периодом
2. Вступление (2-3 предложения)
3. 🔥 Топ-3 темы недели (с кратким описанием)
4. 💡 Инсайт недели (одна ключевая мысль)
5. Вывод и призыв

Стиль: дружеский, технический, без воды.
Длина: 1000-2000 символов.
"""


class PromptManager:
    """Управление промптами."""
    
    @staticmethod
    def format_posts_for_prompt(posts: List[tuple]) -> str:
        """
        Форматировать посты для вставки в промпт.
        
        Args:
            posts: Список кортежей (message_id, text, date)
        
        Returns:
            Отформатированный текст постов
        """
        formatted = []
        for msg_id, text, date in posts:
            formatted.append(f"### Пост {msg_id} ({date.strftime('%Y-%m-%d')}):\n{text}")
        
        return "\n\n---\n\n".join(formatted)
    
    @staticmethod
    def get_generation_prompt(posts: List[tuple]) -> str:
        """Получить промпт для генерации нового поста."""
        posts_text = PromptManager.format_posts_for_prompt(posts)
        return POST_GENERATION_PROMPT.format(posts_text=posts_text)
    
    @staticmethod
    def get_summary_prompt(posts: List[tuple]) -> str:
        """Получить промпт для саммари."""
        posts_text = PromptManager.format_posts_for_prompt(posts)
        return SUMMARY_PROMPT.format(posts_text=posts_text)
    
    @staticmethod
    def get_digest_prompt(posts: List[tuple]) -> str:
        """Получить промпт для дайджеста."""
        posts_text = PromptManager.format_posts_for_prompt(posts)
        return DIGEST_PROMPT.format(posts_text=posts_text)
